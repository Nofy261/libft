#include <string.h>

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
    //if (s[i] == (char)c) // Ne donne pas le meme resultat quand j enleve , pas besoin selon gpt 
    //   return ((char *)&s[i]); 
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		i--;
	}
	return (NULL);
}
